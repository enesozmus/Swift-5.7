/*
    🔴 Printing Constants and Variables

        You can print the current value of a constant or variable with the print(_:separator:terminator:) function:

            print(friendlyWelcome)

        The print(_:separator:terminator:) function is a global function that prints one or more values to an appropriate output.

        In Xcode, for example, the print(_:separator:terminator:) function prints its output in Xcode’s “console” pane.
        The separator and terminator parameter have default values, so you can omit them when you call this function. By default, the function terminates the line it prints by adding a line break.

        To print a value without a line break after it, pass an empty string as the terminator—for example, print(someValue, terminator: "").
*/