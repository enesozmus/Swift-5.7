/*
    🔴 Comments

        Use comments to include nonexecutable text in your code, as a note or reminder to yourself. Comments are ignored by the Swift compiler when your code is compiled. Comments in Swift are very similar to comments in C.
        Single-line comments begin with two forward-slashes (//):

            // This is a comment.

            /* This is also a comment
            but is written over multiple lines. */

    🔴 Semicolons

        Unlike many other languages, Swift doesn’t require you to write a semicolon (;) after each statement in your code, although you can do so if you wish.

    ❗️ However, semicolons are required if you want to write multiple separate statements on a single line:

            let cat = "🐱 "; print(cat)
*/